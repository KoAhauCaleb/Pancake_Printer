/**
* @brief Function implementing the parserTask thread.
* @param argument: Not used
* @retval None
*/
/* USER CODE END Header_StartParserTask */
void StartParserTask(void *argument)
{
  /* USER CODE BEGIN StartParserTask */
  /* Infinite loop */

  BYTE line[50];

  bool complete = false;

  do {
    if(osMessageQueueGetCount(commandQueueHandle) < 30){
      GetLine(line);
      complete = eof();
      myprintf((const char*)line);
      InterpretLine((const char*)line);
    }
    else{
      osDelay(1);
    }
  }
  while(!complete);

  CloseFile();
  for(;;)
  {
    osDelay(1);
  }
  /* USER CODE END StartParserTask */
}

